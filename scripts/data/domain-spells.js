export function getInitialDomainSpell(domain) {
  const slug = normalizeDomainSlug(domain);
  return slug ? (DOMAIN_INITIAL_SPELLS[slug] ?? null) : null;
}

function normalizeDomainSlug(value) {
  return String(value ?? '')
    .trim()
    .toLowerCase()
    .replace(/-apocryphal$/u, '')
    .replace(/[^a-z0-9]+/gu, '-')
    .replace(/^-|-$/gu, '');
}
